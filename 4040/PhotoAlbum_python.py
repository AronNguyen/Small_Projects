class PhotoAlbum():

    def _init_(self):
        self.numberOfPages = 16

    def __init__(x):
        self.numberOfPages = x

    def GetNumberOfPages():
        return self.numberOfPages;

class BigPhotoAlbum(PhotoAlbum):
    def __init__(self):
        self.numberOfPages = 64

    def GetNumberOfPages():
        return self.numberOfPages
